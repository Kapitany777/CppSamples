#include <iostream>

using namespace std;

int main()
{
	cout << "5 + 4 = " << 5 + 4 << endl;
	cout << "5 - 4 = " << 5 - 4 << endl;
	cout << "5 * 4 = " << 5 * 4 << endl;
	cout << "5 / 4 = " << 5 / 4 << endl;
	cout << "5 % 4 = " << 5 % 4 << endl;

	cout << "5 + 4 * 8 = " << 5 + 4 * 8 << endl;
	cout << "(5 + 4) * 8 = " << (5 + 4) * 8 << endl;
	
	return 0;
}
